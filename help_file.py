def show_help():
    """Displays the help menu with available commands."""
    print("\n===== HELP MENU =====")
    print("Available Commands:")
    print(" - 'north', 'south', 'east', 'west' to move between rooms.")
    print(" - 'talk' to talk to a character if one is in the room.")
    print(" - 'fight' to engage in combat with an enemy (you will need an item).")
    print(" - 'take' to pick up an item in the room.")
    print(" - 'heal' to aid a wounded character.")           
    print(" - 'inventory' to check what items you are carrying.")
    print(" - 'help' to display this help menu.")
    print(" - 'quit' to exit the game.")
    print("=====================\n")